#include "loops.h"

/**
 *add - adds numbers
 *@a: first number
 *@b: second number
 *Return: sum of a and b
 */
double add(int a, int b)
{
double sum;

sum = a + b;
return (sum);
}

/**
 *sub - subtracts input number
 *@a: first number
 *@b: second number
 *Return: double
 */
double sub(int a, int b)
{
double sub;

sub = a - b;
return (sub);
}

/**
 *mult - multiplies input number
 *@a: first number
 *@b: second numbebr
 *Return: double
 */
double mult(int a, int b)
{
double mult;

mult = a * b;
return (mult);
}

/**
 *_div - divides input number
 *@a: first number
 *@b: second number
 *Return: double
 */
double _div(int a, int b)
{
double _div;

_div = a / b;
return (_div);
}
